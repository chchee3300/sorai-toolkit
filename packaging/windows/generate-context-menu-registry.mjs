// Generates packaging/windows/context-menu.generated.iss, a [Registry]
// section body that installer.iss pulls in via #include. Regenerated
// fresh by build.ps1 before every ISCC compile -- gitignored, never goes
// stale in the repo the way a hand-maintained copy would.
//
// Mechanism confirmed empirically (real Windows Explorer, real registry
// writes, see the plan doc's Windows spike): a verb key with
// SubCommands="" and its children under a nested `shell` subkey DOES
// cascade correctly. MultiSelectModel="Player" does NOT bundle a multi-
// selection's paths into one invocation the way the original design
// assumed -- it silently no-ops instead (that value is for media-
// player-style "reuse a running instance", not for delivering multiple
// paths in one command line; a real COM IContextMenu extension is the
// only way to get that). So every leaf verb here uses the *default*
// model: Explorer invokes the command once PER SELECTED FILE. The app's
// own single-instance queue+debounce mechanism (src/lib/instanceLock.js,
// src/hooks/useSingleInstance.js) is what turns N per-file invocations
// from one multi-select click back into one batched load.
//
// Format lists mirror sorai-toolkit-converter's useSettings.js
// (VIDEO_FORMATS/IMAGE_FORMATS/AUDIO_FORMATS/TARGET_SIZE_PRESETS_MB) and
// useFileManager.js (VIDEO_EXTS/IMAGE_EXTS/AUDIO_EXTS/PDF_EXTS) by hand --
// no automated cross-repo sync check exists. If Converter's format lists
// change, this file needs a matching manual update or the generated
// context menu silently drifts out of sync with what the app actually
// supports.
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT_PATH = path.join(__dirname, 'context-menu.generated.iss')

const VIDEO_FORMATS = ['.mp4', '.mkv', '.webm', '.avi', '.gif']
const IMAGE_FORMATS = ['.jpg', '.png', '.webp', '.avif', '.ico', '.pdf']
const AUDIO_FORMATS = ['.mp3', '.wav', '.aac', '.flac', '.ogg']
const TARGET_SIZE_PRESETS_MB = [5, 25, 50]

// Input-extension -> category, broader than the output format lists
// above (matches useFileManager.js's getFileType) -- e.g. .mov/.jpeg are
// valid inputs but not output format options, and .gif is an IMAGE input
// despite also being a VIDEO output format (classified by getFileType,
// not by useSettings.js's output lists -- a critical distinction, see
// below).
const INPUT_EXTENSIONS = {
  video: ['mp4', 'mkv', 'avi', 'mov', 'webm'],
  image: ['jpg', 'jpeg', 'png', 'webp', 'avif', 'gif', 'ico'],
  audio: ['mp3', 'wav', 'aac', 'flac', 'ogg'],
  pdf: ['pdf'],
}

const FORMAT_LABELS = {
  '.mp4': 'MP4', '.mkv': 'MKV', '.webm': 'WEBM', '.avi': 'AVI', '.gif': 'GIF',
  '.jpg': 'JPG', '.png': 'PNG', '.webp': 'WEBP', '.avif': 'AVIF', '.ico': 'ICO', '.pdf': 'PDF',
  '.mp3': 'MP3', '.wav': 'WAV', '.aac': 'AAC', '.flac': 'FLAC', '.ogg': 'OGG',
}

// Target-format list for a given input extension: the category's output
// format list minus the input's own equivalent (so a right-clicked .mp3
// never offers "Convert to MP3"). .jpeg is treated as .jpg's equivalent
// (neither offers a "convert to jpg" target from a .jpeg file, since
// they're the same format). Inputs with no real output equivalent (.mov,
// .gif-the-image-input) get the full unfiltered list.
function targetFormatsFor(ext, category) {
  const allFormats = { video: VIDEO_FORMATS, image: IMAGE_FORMATS, audio: AUDIO_FORMATS }[category]
  if (!allFormats) return []
  const ownFormat = ext === 'jpeg' ? '.jpg' : `.${ext}`
  return allFormats.filter((f) => f !== ownFormat)
}

function escapeRegString(s) {
  return s.replace(/"/g, '""')
}

function regLine({ subkey, valueName, value, flags }) {
  const valueNamePart = valueName === '' ? 'ValueName: ""' : `ValueName: "${valueName}"`
  const flagsPart = flags ? `; Flags: ${flags}` : ''
  return `Root: HKCU; Subkey: "${subkey}"; ValueType: string; ${valueNamePart}; ValueData: "${escapeRegString(value)}"${flagsPart}`
}

const lines = []
lines.push('; Auto-generated by packaging/windows/generate-context-menu-registry.mjs -- do not edit by hand.')
lines.push('; Registered per-user (HKCU), matching installer.iss\'s PrivilegesRequired=lowest.')
lines.push('')

for (const [category, exts] of Object.entries(INPUT_EXTENSIONS)) {
  for (const ext of exts) {
    const extKey = `.${ext}`
    const root = `Software\\Classes\\${extKey}\\shell\\SORAIToolkit`

    lines.push(`; --- ${extKey} (${category}) ---`)
    lines.push(regLine({ subkey: root, valueName: 'MUIVerb', value: 'SORAI Toolkit', flags: 'uninsdeletekey' }))
    lines.push(regLine({ subkey: root, valueName: 'Icon', value: '"{app}\\sorai-toolkit.exe",0' }))
    lines.push(regLine({ subkey: root, valueName: 'SubCommands', value: '' }))

    if (category === 'pdf') {
      const compressKey = `${root}\\shell\\CompressPdf`
      lines.push(regLine({ subkey: compressKey, valueName: 'MUIVerb', value: '壓縮 PDF' }))
      lines.push(regLine({
        subkey: `${compressKey}\\command`,
        valueName: '',
        value: '"{app}\\sorai-toolkit.exe" --sorai-quick-action=compress-pdf %1',
      }))
      lines.push('')
      continue
    }

    // 轉檔 submenu -- one leaf per target format.
    const convertKey = `${root}\\shell\\Convert`
    lines.push(regLine({ subkey: convertKey, valueName: 'MUIVerb', value: '轉檔' }))
    lines.push(regLine({ subkey: convertKey, valueName: 'SubCommands', value: '' }))

    for (const targetFormat of targetFormatsFor(ext, category)) {
      const label = FORMAT_LABELS[targetFormat] || targetFormat.slice(1).toUpperCase()
      const leafKey = `${convertKey}\\shell\\ConvertTo_${targetFormat.slice(1)}`
      lines.push(regLine({ subkey: leafKey, valueName: 'MUIVerb', value: `轉成 ${label}` }))
      lines.push(regLine({
        subkey: `${leafKey}\\command`,
        valueName: '',
        value: `"{app}\\sorai-toolkit.exe" --sorai-convert-to=${targetFormat} %1`,
      }))
    }

    // Quick-compress siblings -- video only.
    if (category === 'video') {
      for (const mb of TARGET_SIZE_PRESETS_MB) {
        const quickKey = `${root}\\shell\\QuickCompress_${mb}MB`
        lines.push(regLine({ subkey: quickKey, valueName: 'MUIVerb', value: `快速壓縮到 ${mb}MB 以下` }))
        lines.push(regLine({
          subkey: `${quickKey}\\command`,
          valueName: '',
          value: `"{app}\\sorai-toolkit.exe" --sorai-quick-action=compress-under:${mb} %1`,
        }))
      }
    }

    lines.push('')
  }
}

fs.writeFileSync(OUT_PATH, lines.join('\n') + '\n', 'utf8')
console.log(`Wrote ${OUT_PATH} (${lines.filter((l) => l.startsWith('Root:')).length} registry entries)`)
